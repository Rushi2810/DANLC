# Function to calculate the square of a number
def square(num):
    """
    This function takes one parameter: num.
    It returns the square of the given number.
    """
    result = num ** 2  # Squaring the number
    return result

# Calling the square function with one number
output = square(5)

# Display the result
print("The square of the number is:", output)

# Output:
# The square of the number is: 25
